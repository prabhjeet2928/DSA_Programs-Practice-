/*
Given rectangle of length l and b. Print are of rectangle. 

Constraints:
1 <= l <= 10^9
1 <= b <= 10^9

Input Format:
2 space separated integer l and b

Output Format:
Single number which is area of rectangle

Sample Input:
3 4

Sample Output:
12
*/

#include<bits/stdc++.h>
using namespace std;
int main(){
    int l,b;
    cin>>l>>b;
    cout<<l* 1LL * b<<endl;
}