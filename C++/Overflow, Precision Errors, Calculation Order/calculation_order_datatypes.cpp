#include<bits/stdc++.h>
using namespace std;
int main(){
    cout<<7/2<<endl; // 3-> output
    cout<<7/2.0<<endl;  // 3.5-> output
    cout<<'c' + 1<<endl;  // 100-> output

    int a = 3/2;
    cout<<a<<endl;  // 1-> output

    int b = 3/2.0;
    cout<<b<<endl;  // 1-> output

    double c = 3/2;
    cout<<c<<endl;  // 1-> output

    double d = 3/2.0;
    cout<<d<<endl;  // 1.5-> output
}